export function getDemoQuestions(topic: string, count: number): QuestionFull[] {
  const normalizedTopic = topic.trim().toLowerCase();

  // חיפוש לפי התאמה מדויקת
  for (const key of Object.keys(topicQuestionMap)) {
    if (key.toLowerCase() === normalizedTopic) {
      const pool = topicQuestionMap[key];
      return shuffleArray(pool).slice(0, Math.min(count, pool.length));
    }
  }

  // fallback — שאלות כלליות
  const pool = generalQuestions;
  return shuffleArray(pool).slice(0, Math.min(count, pool.length));
}

function shuffleArray<T>(arr: T[]): T[] {
  const copy = [...arr];
  for (let i = copy.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}
